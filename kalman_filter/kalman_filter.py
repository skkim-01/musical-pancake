import numpy as np
import matplotlib.pyplot as plt

def kalman_filter_demo():
    # ---------------------------------------------------------
    # 1. 초기화 (Initialization) - "아무것도 모르는 상태"
    # ---------------------------------------------------------
    dt = 1.0  # 시간 간격 (1초)

    # 상태 벡터 x [위치, 속도]
    # 처음엔 위치 0, 속도 0이라고 가정
    x = np.array([[0], 
                  [0]]) 
    
    # 공분산 행렬 P (오차범위 & 상관관계)
    # 처음엔 아무것도 모르니까 오차를 아주 크게(1000) 잡음
    P = np.array([[1000, 0], 
                  [0, 1000]]) 

    # ---------------------------------------------------------
    # 2. 물리 법칙 정의 (Model) - "예측을 위한 규칙"
    # ---------------------------------------------------------
    # 상태 전이 행렬 F: "다음 위치 = 현재 위치 + 속도*시간"
    F = np.array([[1, dt], 
                  [0, 1]]) 
    
    # 관측 행렬 H: "센서는 [위치, 속도] 중에서 '위치'만 잴 수 있다"
    H = np.array([[1, 0]])

    # ---------------------------------------------------------
    # 3. 노이즈 설정 (튜닝 포인트 ⭐)
    # ---------------------------------------------------------
    # Q: 내 물리 법칙(예측)에 대한 불신 (환경 노이즈)
    Q = np.array([[1, 0], 
                  [0, 1]]) * 0.1
    
    # R: 센서에 대한 불신 (측정 노이즈)
    # 이 값이 클수록 센서를 안 믿고 내 예측을 믿음
    R = np.array([[50]]) # 센서 오차가 꽤 크다고 가정

    # ---------------------------------------------------------
    # 4. 시뮬레이션 시작 (Loop)
    # ---------------------------------------------------------
    # 가상의 실제 움직임 (참값): 속도 5로 등속 운동
    true_positions = [i * 5 for i in range(20)]
    
    # 가상의 센서 데이터 (참값에 노이즈를 섞음)
    measurements = [p + np.random.normal(0, 10) for p in true_positions]

    history_loc = [] # 결과 저장용
    history_vel = []

    print(f"{'Step':<5} | {'Sensor':<10} | {'Est Pos':<10} | {'Est Vel':<10}")
    print("-" * 50)

    for i, z in enumerate(measurements):
        # --- A. 예측 (Predict) : 물리 법칙으로 추측 ---
        x = F @ x            # 1. 위치 = 위치 + 속도 (행렬 곱)
        P = F @ P @ F.T + Q  # 2. 오차(P)도 커짐 (시간이 지났으니 불확실해짐)

        # --- B. 업데이트 (Update) : 센서 값으로 보정 ---
        # 3. 칼만 게인(K) 계산: "누구 말을 더 믿을까?"
        # (수식은 복잡해 보이지만, 결국 P와 R의 비율을 계산하는 것)
        K = P @ H.T @ np.linalg.inv(H @ P @ H.T + R)

        # 4. 실제 보정 (핵심!)
        # error = (센서값 - 내예측값)
        error = z - (H @ x)
        x = x + K @ error    # 내 예측값에 (비율 * 오차)만큼 더해서 수정
        
        # 5. 공분산(P) 업데이트: "보정했으니 오차를 줄이자"
        I = np.eye(2)
        P = (I - K @ H) @ P

        # 결과 저장
        history_loc.append(x[0, 0])
        history_vel.append(x[1, 0])
        
        print(f"{i:<5} | {z:10.2f} | {x[0,0]:10.2f} | {x[1,0]:10.2f}")

    return true_positions, measurements, history_loc

# 실행
true_pos, sensor_data, est_pos = kalman_filter_demo()